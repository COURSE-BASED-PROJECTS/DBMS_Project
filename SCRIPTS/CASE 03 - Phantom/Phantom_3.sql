PROC USP_THEMCHINHANH
	@MST CHAR(20),
	@MACN CHAR(20),
	@DIACHI NVARCHAR(200)
AS
BEGIN TRAN
	DECLARE @DAIDIEN NVARCHAR(50)
	SET @DAIDIEN = (SELECT NGUOIDAIDIEN FROM CHINHANH WHERE MST = @MST AND MACHINHANH = @MACN)

	BEGIN TRY
		IF (NOT EXISTS(SELECT * FROM CHINHANH WHERE MACHINHANH = @MACN))
		BEGIN
			INSERT CHINHANH(MST, NGUOIDAIDIEN, MACHINHANH, DIACHI)
			VALUES (@MST, @DAIDIEN, @MACN, @DIACHI)
		END
		ELSE 
		BEGIN
			PRINT N'CHI NHÁNH NÀY ĐÃ TỒN TẠI'
			ROLLBACK TRAN
			RETURN
		END
		--WAITFOR DELAY '0:0:05'
	END TRY
	BEGIN CATCH
		DECLARE @ErrorMsg VARCHAR(2000)
		SELECT @ErrorMsg = N'Lỗi: ' + ERROR_MESSAGE()
		RAISERROR(@ErrorMsg, 16,1)
		ROLLBACK TRAN
		RETURN
	END CATCH
	PRINT N'ĐÃ THÊM THÀNH CÔNG'
COMMIT TRAN
GO